    def _export_csv(self, state: MarketResearchState, metadata: Optional[Dict[str, Any]] = None) -> bytes:
        """Export competitor comparison and key metrics as CSV"""
        try:
            output = io.StringIO()
            
            # Write metadata header
            if metadata:
                output.write("# Report Metadata\n")
                for key, value in metadata.items():
                    output.write(f"# {key}: {value}\n")
                output.write("\n")
            
            # Export competitor comparison matrix
            competitor_landscape = state.get('competitor_landscape', {})
            feature_comparisons = state.get('feature_comparisons', [])
            
            if competitor_landscape.get('competitors') or feature_comparisons:
                output.write("# Competitor Analysis\n")
                
                # Competitor overview
                competitors = competitor_landscape.get('competitors', [])
                key_players = competitor_landscape.get('key_players', [])
                
                if competitors:
                    writer = csv.writer(output)
                    writer.writerow(['Competitor Type', 'Company Name', 'Market Position'])
                    
                    for competitor in competitors[:10]:  # Limit to top 10
                        competitor_name = competitor if isinstance(competitor, str) else str(competitor)
                        position = 'Key Player' if competitor_name in key_players else 'Competitor'
                        writer.writerow(['Competitor', competitor_name, position])
                    
                    output.write("\n")
                
                # Feature comparison matrix
                if feature_comparisons:
                    output.write("# Feature Comparison Matrix\n")
                    writer = csv.writer(output)
                    
                    # Extract all unique features
                    all_features = set()
                    for comparison in feature_comparisons:
                        if isinstance(comparison, dict) and 'features' in comparison:
                            all_features.update(comparison['features'].keys())
                    
                    if all_features:
                        # Create header
                        header = ['Competitor'] + list(all_features)
                        writer.writerow(header)
                        
                        # Write feature data
                        for comparison in feature_comparisons[:10]:  # Limit to top 10
                            if isinstance(comparison, dict):
                                row = [comparison.get('competitor', 'Unknown')]
                                features = comparison.get('features', {})
                                for feature in all_features:
                                    row.append(features.get(feature, 'N/A'))
                                writer.writerow(row)
                    
                    output.write("\n")
            
            # Export key metrics summary
            output.write("# Key Metrics Summary\n")
            writer = csv.writer(output)
            writer.writerow(['Metric', 'Value', 'Category'])
            
            # Consumer metrics
            consumer_insights = state.get('consumer_insights', {})
            if consumer_insights.get('structured_insights'):
                writer.writerow(['Consumer Insights Count', len(consumer_insights['structured_insights']), 'Consumer Analysis'])
            
            pain_points = state.get('pain_points', [])
            writer.writerow(['Pain Points Identified', len(pain_points), 'Consumer Analysis'])
            
            # Trend metrics
            market_trends = state.get('market_trends', {})
            if market_trends.get('structured_trends'):
                writer.writerow(['Market Trends Identified', len(market_trends['structured_trends']), 'Trend Analysis'])
            
            trend_predictions = state.get('trend_predictions', [])
            writer.writerow(['Trend Predictions', len(trend_predictions), 'Trend Analysis'])
            
            # Competitor metrics
            if competitors:
                writer.writerow(['Competitors Identified', len(competitors), 'Competitor Analysis'])
            
            # Opportunity metrics
            opportunities = state.get('opportunities', [])
            writer.writerow(['Opportunities Identified', len(opportunities), 'Opportunity Analysis'])
            
            white_spaces = state.get('white_spaces', [])
            writer.writerow(['White Spaces Found', len(white_spaces), 'Opportunity Analysis'])
            
            output.write("\n")
            
            # Export opportunities detail
            if opportunities:
                output.write("# Market Opportunities Detail\n")
                writer = csv.writer(output)
                writer.writerow(['Opportunity Title', 'Description', 'Priority', 'Market Size', 'Competition Level'])
                
                for opp in opportunities[:10]:  # Limit to top 10
                    if isinstance(opp, dict):
                        writer.writerow([
                            opp.get('title', 'N/A'),
                            opp.get('description', 'N/A')[:100] + '...' if len(opp.get('description', '')) > 100 else opp.get('description', 'N/A'),
                            opp.get('priority', 'N/A'),
                            opp.get('market_size', 'N/A'),
                            opp.get('competition_level', 'N/A')
                        ])
            
            # Add export timestamp
            output.write(f"\n# Exported on {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
            
            return output.getvalue().encode('utf-8')
            
        except Exception as e:
            logger.error(f"Error generating CSV export: {e}")
            error_csv = f"Error,Message,Timestamp\nCSV Export Failed,{str(e)},{datetime.now().isoformat()}\n"
            return error_csv.encode('utf-8')
    
    def _extract_confidence_scores(self, state: MarketResearchState) -> Dict[str, float]:
        """Extract confidence scores from analysis results"""
        confidence_scores = {}
        
        # Consumer analysis confidence
        consumer_insights = state.get('consumer_insights', {})
        if consumer_insights.get('structured_insights'):
            scores = [insight.get('confidence_score', 0) for insight in consumer_insights['structured_insights']]
            confidence_scores['consumer_analysis'] = sum(scores) / len(scores) if scores else 0.0
        
        # Trend analysis confidence
        market_trends = state.get('market_trends', {})
        if market_trends.get('structured_trends'):
            scores = [trend.get('confidence_score', 0) for trend in market_trends['structured_trends']]
            confidence_scores['trend_analysis'] = sum(scores) / len(scores) if scores else 0.0
        
        # Overall confidence
        if confidence_scores:
            confidence_scores['overall'] = sum(confidence_scores.values()) / len(confidence_scores)
        
        return confidence_scores
    
