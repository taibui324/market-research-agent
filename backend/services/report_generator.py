    def generate_export_filename(self, state: MarketResearchState, format_type: str) -> str:
        """Generate appropriate filename for export format"""
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        market = state.get('target_market', 'unknown').replace(' ', '_').lower()
        market = re.sub(r'[^\w\-_]', '', market)  # Remove special characters
        
        base_filename = f"3c_analysis_{market}_{timestamp}"
        
        extensions = {
            'markdown': '.md',
            'html': '.html',
            'pdf': '.pdf',
            'json': '.json',
            'csv': '.csv'
        }
        
        return base_filename + extensions.get(format_type, '.txt')