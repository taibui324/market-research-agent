    def _export_pdf(self, report_content: str, metadata: Optional[Dict[str, Any]] = None) -> bytes:
        """Export report as PDF using ReportLab"""
        if not REPORTLAB_AVAILABLE:
            logger.warning("ReportLab not available, returning text-based PDF placeholder")
            return self._export_pdf_fallback(report_content, metadata)
        
        try:
            # Create PDF buffer
            buffer = io.BytesIO()
            
            # Create PDF document
            doc = SimpleDocTemplate(
                buffer,
                pagesize=A4,
                rightMargin=72,
                leftMargin=72,
                topMargin=72,
                bottomMargin=18
            )
            
            # Get styles
            styles = getSampleStyleSheet()
            
            # Create custom styles
            title_style = ParagraphStyle(
                'CustomTitle',
                parent=styles['Heading1'],
                fontSize=24,
                spaceAfter=30,
                alignment=TA_CENTER,
                textColor=colors.HexColor('#2c3e50')
            )
            
            heading_style = ParagraphStyle(
                'CustomHeading',
                parent=styles['Heading2'],
                fontSize=16,
                spaceAfter=12,
                spaceBefore=20,
                textColor=colors.HexColor('#34495e')
            )
            
            body_style = ParagraphStyle(
                'CustomBody',
                parent=styles['Normal'],
                fontSize=11,
                spaceAfter=12,
                alignment=TA_JUSTIFY
            )
            
            # Build PDF content
            story = []
            
            # Add metadata if provided
            if metadata:
                story.append(Paragraph("Report Metadata", heading_style))
                for key, value in metadata.items():
                    story.append(Paragraph(f"<b>{key}:</b> {value}", body_style))
                story.append(Spacer(1, 20))
            
            # Process markdown content
            lines = report_content.split('\n')
            current_paragraph = []
            
            for line in lines:
                line = line.strip()
                
                if not line:
                    if current_paragraph:
                        story.append(Paragraph(' '.join(current_paragraph), body_style))
                        current_paragraph = []
                    continue
                
                # Handle headers
                if line.startswith('# '):
                    if current_paragraph:
                        story.append(Paragraph(' '.join(current_paragraph), body_style))
                        current_paragraph = []
                    story.append(Paragraph(line[2:], title_style))
                elif line.startswith('## '):
                    if current_paragraph:
                        story.append(Paragraph(' '.join(current_paragraph), body_style))
                        current_paragraph = []
                    story.append(Paragraph(line[3:], heading_style))
                elif line.startswith('### '):
                    if current_paragraph:
                        story.append(Paragraph(' '.join(current_paragraph), body_style))
                        current_paragraph = []
                    story.append(Paragraph(f"<b>{line[4:]}</b>", body_style))
                elif line.startswith('---'):
                    if current_paragraph:
                        story.append(Paragraph(' '.join(current_paragraph), body_style))
                        current_paragraph = []
                    story.append(Spacer(1, 12))
                else:
                    # Handle bullet points
                    if line.startswith('- ') or line.startswith('* '):
                        if current_paragraph:
                            story.append(Paragraph(' '.join(current_paragraph), body_style))
                            current_paragraph = []
                        story.append(Paragraph(f"• {line[2:]}", body_style))
                    elif line.startswith(('1. ', '2. ', '3. ', '4. ', '5. ')):
                        if current_paragraph:
                            story.append(Paragraph(' '.join(current_paragraph), body_style))
                            current_paragraph = []
                        story.append(Paragraph(line, body_style))
                    else:
                        current_paragraph.append(line)
            
            # Add any remaining paragraph
            if current_paragraph:
                story.append(Paragraph(' '.join(current_paragraph), body_style))
            
            # Add footer
            story.append(Spacer(1, 30))
            story.append(Paragraph(
                f"Generated by AI-powered 3C Analysis System on {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
                styles['Normal']
            ))
            
            # Build PDF
            doc.build(story)
            
            # Get PDF bytes
            pdf_bytes = buffer.getvalue()
            buffer.close()
            
            return pdf_bytes
            
        except Exception as e:
            logger.error(f"Error generating PDF: {e}")
            # Return error message as simple PDF
            buffer = io.BytesIO()
            doc = SimpleDocTemplate(buffer, pagesize=letter)
            styles = getSampleStyleSheet()
            story = [
                Paragraph("PDF Export Error", styles['Title']),
                Paragraph(f"An error occurred while generating the PDF: {str(e)}", styles['Normal']),
                Paragraph("Please try exporting in a different format.", styles['Normal'])
            ]
            doc.build(story)
            pdf_bytes = buffer.getvalue()
            buffer.close()
            return pdf_bytes
    
