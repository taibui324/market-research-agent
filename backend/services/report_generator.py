    async def generate_3c_report(self, state: MarketResearchState) -> str:
        """
        Generate a comprehensive 3C analysis report from the market research state.
        
        Args:
            state: MarketResearchState containing all analysis results
            
        Returns:
            str: Formatted markdown report
        """
        try:
            logger.info(f"Generating 3C report for {state.get('target_market', 'Unknown Market')}")
            
            # Generate unique report ID
            report_id = self._generate_report_id(state)
            
            # Synthesize insights and remove duplicates
            synthesized_data = await self._synthesize_insights(state)
            
            # Build report sections
            sections = []
            
            # Header section
            sections.append(self._generate_header_section(state, report_id))
            
            # Executive summary
            sections.append(await self._generate_executive_summary(state, synthesized_data))
            
            # Consumer analysis section
            sections.append(await self._generate_consumer_section(state, synthesized_data))
            
            # Trend analysis section
            sections.append(await self._generate_trend_section(state, synthesized_data))
            
            # Competitor analysis section
            sections.append(await self._generate_competitor_section(state, synthesized_data))
            
            # Opportunity analysis section
            sections.append(await self._generate_opportunity_section(state, synthesized_data))
            
            # Footer section
            sections.append(self._generate_footer_section(state, synthesized_data))
            
            # Combine all sections
            full_report = "\n".join(sections)
            
            logger.info(f"Successfully generated 3C report ({len(full_report)} characters)")
            return full_report
            
        except Exception as e:
            logger.error(f"Error generating 3C report: {e}")
            return self._generate_error_report(state, str(e))
    
    def _generate_report_id(self, state: MarketResearchState) -> str:
        """Generate a unique report identifier"""
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        market = state.get('target_market', 'unknown').replace(' ', '_').lower()
        return f"3c_{market}_{timestamp}"
    
    async def _synthesize_insights(self, state: MarketResearchState) -> Dict[str, Any]:
        """
        Synthesize and deduplicate insights from all analysis agents.
        
        Returns:
            Dict containing synthesized and deduplicated data
        """
        try:
            synthesized = {
                'consumer_insights': self._deduplicate_consumer_insights(state),
                'market_trends': self._deduplicate_market_trends(state),
                'competitor_profiles': self._deduplicate_competitor_data(state),
                'opportunities': self._deduplicate_opportunities(state),
                'data_quality': self._assess_data_quality(state),
                'key_metrics': self._calculate_key_metrics(state)
            }
            
            return synthesized
            
        except Exception as e:
            logger.error(f"Error synthesizing insights: {e}")
            
            # Return synthesized data with error handling
            return {
                'consumer_insights': self._safe_deduplicate_consumer_insights(state),
                'market_trends': self._safe_deduplicate_market_trends(state),
                'competitor_profiles': self._handle_competitor_analysis_error(state, e),
                'opportunities': self._safe_deduplicate_opportunities(state),
                'data_quality': self._assess_data_quality(state),
                'key_metrics': self._calculate_key_metrics(state)
            }
    
