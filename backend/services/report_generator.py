    def export_to_format(self, report_content: str, format_type: str = 'markdown', 
                        state: Optional[MarketResearchState] = None, 
                        report_metadata: Optional[Dict[str, Any]] = None) -> bytes:
        """
        Export report to different formats with proper formatting and metadata.
        
        Args:
            report_content: The generated markdown report
            format_type: Target format ('markdown', 'html', 'pdf', 'json', 'csv')
            state: MarketResearchState for structured data export
            report_metadata: Additional metadata for the report
            
        Returns:
            bytes: Formatted report content as bytes
        """
        try:
            if format_type == 'markdown':
                return self._export_markdown(report_content, report_metadata)
            elif format_type == 'html':
                return self._export_html(report_content, report_metadata)
            elif format_type == 'pdf':
                return self._export_pdf(report_content, report_metadata)
            elif format_type == 'json':
                if state is None:
                    raise ValueError("State is required for JSON export")
                return self._export_json(state, report_metadata)
            elif format_type == 'csv':
                if state is None:
                    raise ValueError("State is required for CSV export")
                return self._export_csv(state, report_metadata)
            else:
                raise ValueError(f"Unsupported format: {format_type}")
        except Exception as e:
            logger.error(f"Error exporting to {format_type}: {e}")
            raise
    
    def _export_markdown(self, report_content: str, metadata: Optional[Dict[str, Any]] = None) -> bytes:
        """Export report as enhanced Markdown with metadata"""
        enhanced_content = []
        
        # Add metadata header if provided
        if metadata:
            enhanced_content.append("---")
            enhanced_content.append("# Report Metadata")
            for key, value in metadata.items():
                enhanced_content.append(f"{key}: {value}")
            enhanced_content.append("---")
            enhanced_content.append("")
        
        enhanced_content.append(report_content)
        
        # Add export timestamp
        enhanced_content.append("")
        enhanced_content.append("---")
        enhanced_content.append(f"*Exported on {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}*")
        
        return "\n".join(enhanced_content).encode('utf-8')
    
    def _export_html(self, report_content: str, metadata: Optional[Dict[str, Any]] = None) -> bytes:
        """Export report as properly formatted HTML"""
        try:
            if not MARKDOWN_AVAILABLE or not JINJA2_AVAILABLE:
                return self._export_html_fallback(report_content, metadata)
            
            # Convert markdown to HTML
            md = markdown.Markdown(extensions=['tables', 'fenced_code', 'toc'])
            html_content = md.convert(report_content)
            
            # Extract title from content
            title_match = re.search(r'^# (.+)', report_content, re.MULTILINE)
            title = title_match.group(1) if title_match else "3C Analysis Report"
            
            # Create template context
            template_context = {
                'title': title,
                'content': html_content,
                'metadata': metadata or {},
                'export_timestamp': datetime.now().strftime('%Y-%m-%d %H:%M:%S')
            }
            
            # Render HTML template
            template = Template(self.html_template)
            final_html = template.render(**template_context)
            
            return final_html.encode('utf-8')
            
        except Exception as e:
            logger.error(f"Error converting to HTML: {e}")
            return self._export_html_fallback(report_content, metadata)
    
